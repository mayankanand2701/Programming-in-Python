# Independent events are events where the occurrence of one event does not affect the probability of the
# occurrence of another event. In other words, the outcome of one event has no influence on the outcome of the
# other event.

# Examples of independent events:

# 1.Tossing a fair coin: The outcome of one coin toss (e.g., getting heads) does not affect the outcome of 
# subsequent coin tosses. Each toss is independent of the others, and the probability of getting heads
# on the second toss is still 0.5, regardless of whether heads or tails was obtained on the first toss.

# 2.Drawing cards from a well-shuffled deck: Suppose you draw a card from a standard deck of 52 playing cards 
# without replacement. The probability of drawing a specific card (e.g., the Ace of Spades) on the second draw
# is not influenced by the outcome of the first draw. Each draw is independent, and the probability of drawing 
# any particular card remains constant throughout the process.


